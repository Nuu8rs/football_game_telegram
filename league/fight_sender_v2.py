    async def _send_messages(self,
                             characters: list[Character],
                             text: str,
                             photo: FSInputFile | None = None,
                             keyboard: Optional[InlineKeyboardButton] = None
                             ) -> None:
        
        for character in characters:
            await self.__send_message(
                character = character,
                text      = text,
                photo     = photo,
                keyboard  = keyboard
            )
              
