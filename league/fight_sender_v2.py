            return (
                {'club_name': self.clubs_in_match.second_club.name_club, 'goals': self.clubs_in_match.goals_second_club},
                {'club_name': self.clubs_in_match.first_club.name_club, 'goals': self.clubs_in_match.goals_first_club}
            )

